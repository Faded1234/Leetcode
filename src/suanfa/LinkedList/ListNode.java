public class ListNode {
    public int val;
    public ListNode next;

    public ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return "suanfa.LinkedList.ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
